    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            int pilagama=0, pilpendidikan = 0, pilperkawinan = 0, pilkwn = 0;
            String sAgama="", sPendidikan="", sPerkawinan="", skwn="";
            String sJenisKelamin;

            pilagama = cAgama.getSelectedIndex();
            if (pilagama==0) { sAgama="Islam";}
            if (pilagama==1) { sAgama="Hindu";}
            if (pilagama==2) { sAgama="Budha";}
            if (pilagama==3) { sAgama="Kristen";}
            if (pilagama==4) { sAgama="Katolik";}
            if (pilagama==5) { sAgama="Lainnya";}

            sdf = new SimpleDateFormat("yyyy-M-d");

            if (rLaki.isSelected()){
                sJenisKelamin="laki-laki";
            } else{
                sJenisKelamin="Perempuan";
            }

            pilpendidikan = cPendidikan.getSelectedIndex();
            if (pilpendidikan==0) { sPendidikan="Tidak Sekolah";}
            if (pilpendidikan==1) { sPendidikan="SD";}
            if (pilpendidikan==2) { sPendidikan="SMP";}
            if (pilpendidikan==3) { sPendidikan="SMA";}
            if (pilpendidikan==4) { sPendidikan="D3";}
            if (pilpendidikan==5) { sPendidikan="S1";}
            if (pilpendidikan==6) { sPendidikan="S2";}
            if (pilpendidikan==7) { sPendidikan="S3";}

            pilperkawinan = cPerkawinan.getSelectedIndex();
            if (pilperkawinan==0) { sPerkawinan="belum kawin";}
            if (pilperkawinan==1) { sPerkawinan="kawin";}
            if (pilperkawinan==2) { sPerkawinan="cerai hidup";}
            if (pilperkawinan==3) { sPerkawinan="cerai mati";}

            pilkwn = cKwn.getSelectedIndex();
            if (pilkwn==0) { skwn="WNI";}
            if (pilkwn==1) { skwn="WNA";}

            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO data_penduduk(nik_ktp, nama, tempat_lahir, tgl_lahir, jenis_kelamin, "
                + "gol_darah, alamat, rt_rw, dusun_desa, kelurahan, kecamatan, kode_pos, agama, pendidikan, status_kawin, pekerjaan, kewarganegaraan, no_kk, status_keluarga) values(?,?,"
                + "?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            stmt.setString(1, txtNik.getText());
            stmt.setString(2, txtnama.getText());
            stmt.setString(3, txtTempat.getText());
            stmt.setString(4, sdf.format(txtTanggal.getDate()));
            stmt.setString(5, sJenisKelamin);
            stmt.setString(6, txtGol.getText());
            stmt.setString(7, txtAlamat.getText());
            stmt.setString(8, txtRw.getText());
            stmt.setString(9, txtDusun.getText());
            stmt.setString(10, txtKelurahan.getText());
            stmt.setString(11, txtKecamatan.getText());
            stmt.setString(12, txtPos.getText());
            stmt.setString(13, sAgama);
            stmt.setString(14, sPendidikan);
            stmt.setString(15, sPerkawinan);
            stmt.setString(16, txtPekerjaan.getText());
            stmt.setString(17, skwn);
            stmt.setString(18, txtKk.getText());
            stmt.setString(19, txtKeluarga.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            clearData();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

