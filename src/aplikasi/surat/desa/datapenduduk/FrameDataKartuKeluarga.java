    public void TampilData() {
        DftTblModel_data = new DefaultTableModel();
        DftTblModel_data.addColumn("NO KK");
        DftTblModel_data.addColumn("NAMA KEPALA");
        DftTblModel_data.addColumn("NIK KEPALA");
        DftTblModel_data.addColumn("ALAMAT");
        DftTblModel_data.addColumn("DUSUN");
        DftTblModel_data.addColumn("KELURAHAN");
        DftTblModel_data.addColumn("KECAMATAN");
        tbData.setModel(DftTblModel_data);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM data_kartu_keluarga";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_data.addRow(new Object[]{
                    res.getString("no_kk"),
                    res.getString("nik_ktp_kepala_keluarga"),
                    res.getString("nama_kepala_keluarga"),
                    res.getString("alamat"),
                    res.getString("dusun_desa"),
                    res.getString("kelurahan"),
                    res.getString("kecamatan")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

