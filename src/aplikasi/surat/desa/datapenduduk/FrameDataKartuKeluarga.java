    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO data_kartu_keluarga(no_kk, nik_ktp_kepala_keluarga, nama_kepala_keluarga, alamat, rt_rw, dusun_desa, kelurahan, kecamatan, kode_pos) values(?,?,"
                + "?,?,?,?,?,?,?)");
            stmt.setString(1, txtNoKK.getText());
            stmt.setString(2, txtNik.getText());
            stmt.setString(3, txtNama.getText());
            stmt.setString(4, txtAlamat.getText());
            stmt.setString(5, txtRw.getText());
            stmt.setString(6, txtDesa.getText());
            stmt.setString(7, txtKelurahan.getText());
            stmt.setString(8, txtKecamatan.getText());
            stmt.setString(9, txtPos.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            clearData();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

